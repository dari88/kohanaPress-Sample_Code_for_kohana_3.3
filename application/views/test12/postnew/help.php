        <div class="contextual-help-tabs-wrap">

            <div id="tab-panel-customize-display" class="active help-tab-content">
                <p>タイトルフィールドと大きな投稿編集エリアは所定の位置に固定されていますが、ドラッグアンドドロップであらゆる場所に位置を変えたり、それぞれのボックスのタイトルバーをクリックして最小化・最大化することができます。より多くのボックス（概要、送信したトラックバック、カスタムフィールド、ディスカッション、スラッグ、作成者）を隠したり、この画面のレイアウトを1カラムか2カラムから選ぶには表示オプションタブを使ってください。</p>							</div>

            <div id="tab-panel-title-post-editor" class=" help-tab-content">
                <p><strong>タイトル</strong> - 投稿のタイトルを入力してください。入力後、編集できるパーマリンクがすぐ下に表示されます。</p><p><strong>投稿エディタ</strong> - 投稿の文章を入力してください。編集にはビジュアルと HTML という2つのモードがあります。タブをクリックして選択してください。ビジュアルエディタの右端のアイコンをクリックすると、さらに多くのアイコンが表示されます。HTML モードでは、テキストと一緒に生の HTML コードを入力できます。投稿エディタ上のアイコンをクリックして指示に従うと、メディアファイルを挿入できます。全画面表示アイコン (ビジュアルエディタでは右から2番目、HTML エディタでは右端) をクリックすると、バージョン 3.2 で導入された集中執筆モードも使えます。このモードに入ったら、上部エリアをマウスオーバーすることでボタンを表示できます。通常のエディタに戻るには全画面表示から退出してください。</p>							</div>

            <div id="tab-panel-publish-box" class=" help-tab-content">
                <p><strong>公開</strong> - 公開ボックスで、公開条件を設定できます。ステータスや表示状態を決定したり、すぐに公開するには、「編集」リンクをクリックしてオプションを表示させます。表示状態として、投稿のパスワード保護や、投稿をサイトの最初にずっと表示させる (固定表示) ための設定などが行えます。すぐに公開するという設定を変更することで、投稿の公開日時を過去または未来のものに変更できるため、公開予約や投稿の日付を過去にすることが可能です。</p><p><strong>投稿フォーマット</strong> - テーマが特定の投稿をどのように表示するかを指定します。たとえば、タイトルと文章を含む<em>標準</em>フォーマットのブログ投稿、タイトルを省略し短いテキストのみを含む短い<em>アサイド</em>などです。詳しくは Codex の <a href="http://wpdocs.sourceforge.jp/Post_Formats#Supported_Formats">各投稿フォーマットについての説明</a>をご覧ください。テーマで有効化できる投稿フォーマットの数は10個以内です。</p><p><strong>アイキャッチ画像</strong> - 投稿内に挿入することなく、画像を投稿に関連付けることができます。アイキャッチ画像をホームページでの投稿サムネイルや、カスタムヘッダーなどに使用できるテーマを利用している場合には便利です。</p>							</div>

            <div id="tab-panel-discussion-settings" class=" help-tab-content">
                <p><strong>トラックバックの送信</strong> - トラックバックは、旧式のブログシステムに対してリンクを通知する方法です。トラックバックを送りたい URL を入力してください。他の WordPress サイトにリンクをした場合は、ピンバックで自動的に通知されますのでこの欄の入力は不要です。</p><p><strong>ディスカッション</strong> - コメントやトラックバック/ピンバックを有効化、無効化することができます。投稿にコメントがあれば、ここで読んでモデレートできます。</p>							</div>
